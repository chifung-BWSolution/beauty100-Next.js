  name TEXT NOT NULL UNIQUE,
  region TEXT NOT NULL,
  region_emoji TEXT NOT NULL DEFAULT '',
  sort_order INT NOT NULL DEFAULT 0,
  created_at TIMESTAMPTZ DEFAULT NOW()
);

